func chooseTransition(r *rand.Rand, ts []Transition) *Transition {
	total := 0.0
	for _, t := range ts {
		total += t.Weight
	}

	p := r.Float64() * total
	acc := 0.0

	for _, t := range ts {
		acc += t.Weight
		if p <= acc {
			return &t
		}
	}
	return nil
}

